    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        // TODO add your handling code here:
        int a = sliderA.getValue();
        int b = sliderB.getValue();
        int c = sliderC.getValue();
        if ((a < b + c) && (b < a + c) && (c < a + b)) {
            if (a == b && a == c) {
                lblTipoTriangulo.setText("Forma um triangulo equilÃ¡tero");
            }
         else if ((a == b) || (a == c)) {
            lblTipoTriangulo.setText("Forma um triangulo isosceles");
        } else {
            lblTipoTriangulo.setText("Forma um triangulo escaleno");
        }
        }else {
                lblTipoTriangulo.setText("Nao forma um triangulo");
                }
        
    }//GEN-LAST:event_btnVerificarActionPerformed

